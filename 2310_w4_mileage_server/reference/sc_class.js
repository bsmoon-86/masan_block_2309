// Class : 값과 행위를 갖는 객체
// 값 -> 데이터 -> 변수
// 행위 -> 행동 -> 함수
// class는 변수와 함수의 모음
// 붕어빵 제작 과정 
// 1. 빵틀에 반죽을 붓는다
// 2. 소를 넣는다
// 3. 반죽을 위에 한번 더 붓는다
// 4. 가열한다
// 5. 한번 뒤집는다
// 6. 완성된 붕어빵을 꺼낸다
// 해당하는 과정에서 변수는 ? -> 반죽, 소(팥, 슈크림)
// 클래스를 선언 -> 레시피를 생성(붕어빵의 틀을 제작)
// 클래스를 생성 -> 해당하는 레시피에 재료를 선택(반죽과 소를 선택)
// 클래스 안에 있는 함수를 호출 -> 붕어빵을 제작(팥붕어빵, 슈크림붕어빵)